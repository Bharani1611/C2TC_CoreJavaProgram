package com.tnif.dayTwo;

public class Char {
	public static void main(String[] args) {
		char ch = 'b';
		System.out.println(ch);

		char ch1 = 98;
		System.out.println(ch1);

		char var1 = '\u0042';
		System.out.println(var1);

		char var2 = '\u20AC';
		System.out.println(var2);

		int a = 'B';
		System.out.println(a);
	}
}
